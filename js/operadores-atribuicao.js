// Operadores Aritméticos JavaScript
// + 	Adição
// - 	Subtração
// * 	Multiplicação
// / 	Divisão
// ** 	Potenciação
// % 	Resto
// ++ 	Incrementação
// -- 	Decrementação

// Operadores de Atribuição JavaScript
// = 	Atribuição 	Ex: x = 1 	-> x é 1
// += 	Adição		Ex: x += 1 	-> x = x + 1
// -= 	Subtração	Ex: x -= 1	-> x = x - 1
// *= 	Multiplic.	Ex: x *= 1	-> x = x * 1
// /= 	Divisão		Ex: x /= 1	-> x = x / 1
// **=	Potenciação 	Ex: x **= 1	-> x = x ** 1
// %= 	Resto		Ex: x %= 1	-> x = x % 1


// Operadores Comparativos JavaScript
// == 	Igual
// != 	Diferente
// > 	Maior
// < 	Menor
// >= 	Maior ou Igual
// <= 	Menor ou Igual

// Operadores Lógicos JavaScript
// E And	Ex: 4>5 && 10>5	Ex: 4>5 e 10>5 		-> Resultado Falso
// OU Or 	Ex: 4>5 || 10>5 Ex: 4>5 ou 10>5 	-> Resultado Verdadeiro
// Não Not Ex: !(4>5)	Ex: Não (4>5)		-> Resultado Verdadeiro
// XOR 	Ex: 4>5 ^ 10>5 	Ex: 4>5 xor 10>5 	-> Resultado Verdadeiro


